bool isPrime(int x)
{
    if (x == 1)
        return false;
    for (int i = 2; i * i <= x; i++)
    {
        if (x % i == 0)
            return false;
    }
    return true;
}

void solve()
{
    int n;
    cin >> n;
    vector<int> v;
    int ans = n / 2;
    for (int i = 3; i <= n; i += 2)
    {
        if (isPrime(i))
        {
            v.push_back(i);
            ans += n / i;
        }
    }

    cout << ans << nl;
}

