void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    map<char, int> mp;
    for (int i = 0; i < n; i++)
    {
        mp[s[i]]++;
    }

    bool ok = false;
    for (int i = 1; i < n - 1; i++)
    {
        if (mp[s[i]] > 1)
        {
            ok = true;
            break;
        }
    }

    if (ok)
    {
        cyes;
    }
    else
    {
        cno;
    }
}

