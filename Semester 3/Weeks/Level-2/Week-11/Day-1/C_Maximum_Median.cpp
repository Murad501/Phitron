#define MuRAD_BOOST()                 \
    ios_base::sync_with_stdio(false); \
    cin.tie(NULL);

void solve()
{
    int n, k;
    cin >> n >> k;
    vector<int> v;
    for (int i = 0; i < n; i++)
    {
        int x;
        cin >> x;
        v.push_back(x);
    }

    auto ok = [&](ll median)
    {
        ll cnt = 0;
        for (int i = n / 2; i < n; i++)
        {
            cnt += (v[i] < median ? (median - v[i]) : 0);
        }
        return cnt <= k;
    };

    sort(v.begin(), v.end());
    ll l = 1, r = 2e9, mid, ans;
    while (l <= r)
    {
        mid = l + (r - l) / 2;
        if (ok(mid))
        {
            ans = mid;
            l = mid + 1;
        }
        else
        {
            r = mid - 1;
        }
    }

    cout << ans << nl;
}

int main()
{
    MuRAD_BOOST();
    solve();

    return 0;
}